package com.a16lao.wyh.bean.city;

public class BookListBean {

    private String bookListName;
    private String bookListDescription;
    private String bookListCover;

    public String getBookListName() {
        return bookListName;
    }

    public void setBookListName(String bookListName) {
        this.bookListName = bookListName;
    }

    public String getBookListDescription() {
        return bookListDescription;
    }

    public void setBookListDescription(String bookListDescription) {
        this.bookListDescription = bookListDescription;
    }

    public String getBookListCover() {
        return bookListCover;
    }

    public void setBookListCover(String bookListCover) {
        this.bookListCover = bookListCover;
    }
}
